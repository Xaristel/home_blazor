CREATE DATABASE Finances;
GO

USE Finances;

CREATE TABLE Users
(
  UserId INT NOT NULL PRIMARY KEY IDENTITY(1,1),
  UserName CHAR(30),
  UserPassword CHAR(30)
);
GO

CREATE TABLE Income 
(
  IncomeId INT NOT NULL PRIMARY KEY IDENTITY(1,1), 
  IncomeName CHAR(30),
  IncomeValue INT,
  IncomeDate CHAR(10),
  UserId INT NOT NULL
);
GO

ALTER TABLE Income
WITH CHECK ADD CONSTRAINT FK_INCOME_USERS FOREIGN KEY(UserId)
REFERENCES Users (UserId)
ON UPDATE CASCADE
ON DELETE CASCADE
GO

CREATE TABLE Cost
(
  CostId INT NOT NULL PRIMARY KEY IDENTITY(1,1),
  CostName CHAR(30),
  CostValue INT,
  CostDate CHAR(10),
  UserId INT NOT NULL
);
GO

ALTER TABLE Cost
WITH CHECK ADD CONSTRAINT FK_COST_USERS FOREIGN KEY(UserId)
REFERENCES Users (UserId)
ON UPDATE CASCADE
ON DELETE CASCADE
GO